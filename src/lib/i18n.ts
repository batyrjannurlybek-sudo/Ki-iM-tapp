export const dictionaries = { kk, ru };
export type Dict = typeof kk;

export function getDict(locale: Locale): Dict {
  return dictionaries[locale] ?? dictionaries[defaultLocale];
}

/** Interpolate {key} placeholders in a translation string. */
export function tpl(s: string, vars: Record<string, string | number>): string {
  return s.replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ""));
}

/** Localize a category by its slug, falling back to the DB name. */
export function categoryName(slug: string, fallback: string, dict: Dict): string {
  const key = `cat_${slug}` as keyof Dict;
  return dict[key] ?? fallback;
}

